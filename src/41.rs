// Example Rust code demonstrating basic arithmetic operations
fn main() {
    // Addition
    let num1 = 5;
    let num2 = 3;
    let sum = num1 + num2; // sum of num1 and num2

    println!("The sum is: {}", sum);

    // Subtraction
    let num3 = 7;
    let num4 = 4;
    let difference = num3 - num4;

    println!("The difference is: {}", difference);

    // Multiplication
    let num5 = 8;
    let num6 = 2;
    let product = num5 * num6; // product of num5 and num6

    println!("The product is: {}", product);
}
